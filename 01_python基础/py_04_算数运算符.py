# 加 减 乘 除 除取整 除取余 乘方  ---> + - * / // % **

if __name__ == '__main__':

    # 加法
    print(20 + 10)
    # 减法
    print(20 - 10)
    # 乘法
    print(20 * 10)
    # 除法
    print(30 / 15)
    # 除法取整数
    print(20 // 15)
    # 除法取余数
    print(20 % 15)
    # 乘方
    print(20 ** 2)
    # 在 Python 中 * 运算符还可以用于字符串，计算结果就是字符串重复指定次数的结果
    print("-" * 50)